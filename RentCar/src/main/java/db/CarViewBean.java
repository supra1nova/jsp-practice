package db;

public class CarViewBean {
	
	private String name;
	private int price;
	private String img;
	private int qty;
	private int duration;
	private String date;
	private int assurance;
	private int wifi;
	private int navigation;
	private int baby_seat;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public int getQty() {
		return qty;
	}
	public void setQty(int qty) {
		this.qty = qty;
	}
	public int getDuration() {
		return duration;
	}
	public void setDuration(int duration) {
		this.duration = duration;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public int getAssurance() {
		return assurance;
	}
	public void setAssurance(int assurance) {
		this.assurance = assurance;
	}
	public int getWifi() {
		return wifi;
	}
	public void setWifi(int wifi) {
		this.wifi = wifi;
	}
	public int getNavigation() {
		return navigation;
	}
	public void setNavigation(int navigation) {
		this.navigation = navigation;
	}
	public int getBaby_seat() {
		return baby_seat;
	}
	public void setBaby_seat(int baby_seat) {
		this.baby_seat = baby_seat;
	}
	
}
